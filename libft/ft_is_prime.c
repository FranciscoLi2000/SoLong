#include "libft.h"

int	ft_is_prime(int nbr)
{
	int	i;

	if (nbr < 2)
		return (0);
	i = 2;
	while (i <= nbr / 2)
	{
		if (nbr % i == 0)
			return (0);
		i++;
	}
	return (1);
}
