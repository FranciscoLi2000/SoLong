#include "libft.h"

char	*ft_strrev(char *str)
{
	char	tmp;
	int		len;
	int		i;

	len = 0;
	while (str[len])
		len++;
	i = 0;
	while (i < len - 1)
	{
		tmp = str[i];
		str[i] = str[len - 1];
		str[len - 1] = tmp;
		len--;
		i++;
	}
	return (str);
}
