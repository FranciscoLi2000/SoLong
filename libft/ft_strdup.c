#include "libft.h"

char	*ft_strdup(char *src)
{
	size_t	len;
	size_t	i;
	char	*dup;

	len = ft_strlen(src);
	dup = malloc(len + 1);
	i = 0;
	if (!dup)
		return (NULL);
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
